  // Garde getText tel quel pour tes autres pages
  static String getText(String language, String key) {
    final langData = translations[language] ?? translations['Français']!;
    final value = langData[key];
    return (value is String) ? value : key;
  }

  // getMap amélioré pour ne plus renvoyer 'null' par erreur de frappe
  static Map<String, dynamic>? getMap(String language, String key) {
    final langData = translations[language] ?? translations['Français']!;

    // 1. On nettoie la clé (enlève les espaces et ignore la casse)
    String cleanSearchKey = key.trim().toLowerCase();

    // 2. On cherche dans la Map de la langue choisie
    for (var entry in langData.entries) {
      if (entry.key.trim().toLowerCase() == cleanSearchKey) {
        if (entry.value is Map<String, dynamic>) {
          return entry.value as Map<String, dynamic>;
        }
      }
    }
    return null;
  }
