  static String t(String languageCode, String key) {
    return _values[languageCode]?[key] ?? _values['fr']?[key] ?? key;
  }

  static String currency(String languageCode) =>
      languageCode == 'ar' ? 'دج' : 'DA';

  static String formatPrice(String languageCode, Object? value) =>
      '${value ?? 0} ${currency(languageCode)}';

  static String unit(String languageCode, String? rawUnit) {
    final unit = (rawUnit == null || rawUnit.trim().isEmpty)
        ? 'ml'
        : rawUnit.trim();
    if (languageCode != 'ar') return unit;

    switch (unit.toLowerCase()) {
      case 'ml':
      case 'ml.':
        return 'مل';
      case 'g':
      case 'gr':
      case 'gramme':
      case 'grammes':
        return 'غ';
      case 'pcs':
      case 'pc':
      case 'pièce':
      case 'pieces':
      case 'pièces':
        return 'قطعة';
      default:
        return unit;
    }
  }

  static String formatContents(
    String languageCode,
    Object? contents,
    String? rawUnit,
  ) {
    final value = contents?.toString().trim() ?? '';
    if (value.isEmpty) return '';
    return '$value ${unit(languageCode, rawUnit)}';
  }
